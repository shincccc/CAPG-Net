def get_max_abs_nino_in_window(nino_series, curr_date, lag_min=1, lag_max=60, step=1):
    lags = range(lag_min, lag_max + 1, step)
    values = []
    valid_lags = []

    for lag in lags:
        nino_date = curr_date - pd.Timedelta(days=lag)
        if nino_date in nino_series.index:
            val = nino_series.loc[nino_date]
            values.append(val)
            valid_lags.append(lag)

    if values:
        abs_values = np.abs(values)
        abs_max_idx = np.argmax(abs_values)
        max_idx = np.argmax(values)
        min_idx = np.argmin(values)
        return values[abs_max_idx], valid_lags[abs_max_idx], values[max_idx], valid_lags[max_idx], values[min_idx], \
        valid_lags[min_idx]
    else:
        return 0.0, None, 0.0, None, 0.0, None


class BatchTester:
    def __init__(self, config, model_type_fluid='lstm_enso_gate',
                 forecast_days_list=[30, 90, 180, 365]):
        self.config = config
        self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        self.model_type_fluid = model_type_fluid
        self.forecast_days_list = forecast_days_list

        self.OMEGA1 = 2 * np.pi / 365.2425
        self.OMEGA2 = 2 * np.pi / (365.2425 / 2)

        self.test_years = list(range(2015, 2026))

        print(f"⚡ Device: {self.device}")
        print(f"🧠 Fluid model type: {model_type_fluid}")
        print(f"📅 Test years: {self.test_years[0]} - {self.test_years[-1]}")

        self.results = {}

    def _init_fluid_model(self, forecast_days, lookback=365):
        name = self.model_type_fluid
        if name not in MODEL_REGISTRY:
            raise ValueError(f"Unknown model '{name}'. Available: {sorted(MODEL_REGISTRY.keys())}")

        model = create_model(
            name,
            input_dim=1,
            lookback=lookback,
            forecast_days=forecast_days,
            hidden_dim=64,
            dropout=0.2,
        ).to(self.device)
        return model

    def _load_fluid_weights(self, model, forecast_days):
        """
        🔥 Dynamic routing: auto-locate weight files based on model type
        """
        is_gate_model = ('gate' in self.model_type_fluid)

        if is_gate_model:
            # Main model weight path (generated by train_dual_physics.py)
            dir_path = './weight/fluid_model_weights'
            weight_paths = [
                f"{dir_path}/trained_model_weights_{forecast_days}_{self.model_type_fluid}.pth",
                f"{dir_path}/warmup_initial_weights_{forecast_days}_{self.model_type_fluid}.pth",
            ]
        else:
            # Baseline comparison model weight path (generated by train_dual_physics_fluid_compare.py)
            dir_path = './weight/fluid_compare_weights'
            weight_paths = [
                f"{dir_path}/trained_fluid_{forecast_days}_{self.model_type_fluid}.pth",
                f"{dir_path}/warmup_fluid_{forecast_days}_{self.model_type_fluid}.pth",
            ]

        for path in weight_paths:
            if os.path.exists(path):
                try:
                    checkpoint = torch.load(path, map_location=self.device)
                    model.load_state_dict(checkpoint['model_state_dict'])
                    print(f"   ✅ Loaded weights: {os.path.basename(path)}")
                    return True
                except Exception as e:
                    print(f"   ⚠️ Failed to load ({os.path.basename(path)}): {e}")
                    continue

        print(f"   ❌ Weight file for {forecast_days}-day forecast not found, check directory: {dir_path}")
        return False

    def _compute_core_baseline(self, lod_core_values, idx, forecast_days, fit_window=365):
        t_fit_raw = np.arange(idx - fit_window, idx)
        t_fit_local = t_fit_raw - t_fit_raw[0]

        X_fit = np.column_stack([
            np.ones(fit_window), t_fit_local,
            np.cos(self.OMEGA1 * t_fit_local), np.sin(self.OMEGA1 * t_fit_local),
            np.cos(self.OMEGA2 * t_fit_local), np.sin(self.OMEGA2 * t_fit_local)
        ])

        coeffs = np.linalg.lstsq(X_fit, lod_core_values[idx - fit_window: idx], rcond=1e-6)[0]

        t_future_local = np.arange(idx, idx + forecast_days) - t_fit_raw[0]
        X_future = np.column_stack([
            np.ones(forecast_days), t_future_local,
            np.cos(self.OMEGA1 * t_future_local), np.sin(self.OMEGA1 * t_future_local),
            np.cos(self.OMEGA2 * t_future_local), np.sin(self.OMEGA2 * t_future_local)
        ])

        return X_future @ coeffs

    def prepare_test_data(self, eop_df, ex_df, nino_df, forecast_days, lookback=365):
        lod_fluid = (eop_df['lod_obs'] - eop_df['lod_core']).values
        lod_core = eop_df['lod_core'].values
        eam_values = ex_df['total_eam'].shift(1).fillna(0.0).values
        dates = eop_df['date'].values

        valid_start = max(lookback, 365)
        valid_end = len(eop_df) - forecast_days

        X_fluid_list, X_static_list, base_fluid_list = [], [], []
        dates_list, core_ls_baseline_list = [], []

        MAX_LAG = 60
        MIN_LAG = 1

        for i in range(valid_start, valid_end):
            curr_date = pd.Timestamp(dates[i])

            seq_eam = eam_values[i - lookback: i]
            X_fluid_list.append(seq_eam.reshape(lookback, 1))
            base_fluid_list.append(lod_fluid[i - 1])

            if curr_date in nino_df.index:
                nino_lag_abs_max, _, nino_lag_pos_max, best_lag_pos_max, nino_lag_neg_max, best_lag_neg_max = get_max_abs_nino_in_window(
                    nino_df['nino34'], curr_date, lag_min=MIN_LAG, lag_max=MAX_LAG, step=5
                )

                [nino_lag_pos_max, best_lag_pos_max] = [0, None] if np.abs(nino_lag_abs_max) < 0.5 else [
                    nino_lag_pos_max, best_lag_pos_max]
                [nino_lag_neg_max, best_lag_neg_max] = [0, None] if np.abs(nino_lag_abs_max) < 0.5 else [
                    nino_lag_neg_max, best_lag_neg_max]

                lag_pos_val = best_lag_pos_max if best_lag_pos_max is not None else 0
                lag_neg_val = best_lag_neg_max if best_lag_neg_max is not None else 0

                # pos_pos = np.exp(-lag_pos_val / MAX_LAG)
                # pos_neg = np.exp(-lag_neg_val / MAX_LAG)
                pos_pos = (lag_pos_val - MIN_LAG) / (MAX_LAG - MIN_LAG)
                pos_neg = (lag_neg_val - MIN_LAG) / (MAX_LAG - MIN_LAG)
            else:
                nino_lag_pos_max, pos_pos, nino_lag_neg_max, pos_neg = 0, 0, 0, 0

            day_of_year = curr_date.dayofyear
            season_sin = np.sin(2 * np.pi * day_of_year / 365.25)
            season_cos = np.cos(2 * np.pi * day_of_year / 365.25)

            static_features = np.array([
                nino_lag_pos_max, pos_pos, nino_lag_neg_max, pos_neg, season_sin, season_cos
            ])

            # ablation configurations
            # static_features = np.array([
            #     season_sin, season_cos
            # ])

            X_static_list.append(static_features)

            core_ls_pred = self._compute_core_baseline(lod_core, i, forecast_days, fit_window=365)
            core_ls_baseline_list.append(core_ls_pred)
            dates_list.append(dates[i])

        return (np.array(X_fluid_list), np.array(X_static_list), np.array(base_fluid_list),
                np.array(dates_list), np.array(core_ls_baseline_list))

    def predict_year(self, model_fluid, test_X_f, test_X_static, test_base_f, test_core_ls_baseline, batch_size=1024):
        model_fluid.eval()

        test_dataset = torch.utils.data.TensorDataset(
            torch.FloatTensor(test_X_f),
            torch.FloatTensor(test_X_static)
        )
        test_loader = torch.utils.data.DataLoader(
            test_dataset, batch_size=batch_size, shuffle=False, drop_last=False
        )

        pred_fluid_list = []
        with torch.no_grad():
            for batch_X, batch_static in test_loader:
                batch_X = batch_X.to(self.device)
                batch_static = batch_static.to(self.device)

                # 🔥 Dynamic routing: single-input vs dual-input adaptation
                if 'gate' in self.model_type_fluid:
                    outputs = model_fluid(batch_X, batch_static)
                else:
                    outputs = model_fluid(batch_X)

                if isinstance(outputs, tuple):
                    pred = outputs[0]
                else:
                    pred = outputs
                pred_fluid_list.append(pred.cpu().numpy())

        pred_fluid_delta = np.concatenate(pred_fluid_list, axis=0)
        pred_fluid_abs = pred_fluid_delta + test_base_f[:, np.newaxis]
        pred_core_abs = test_core_ls_baseline
        pred_lod_seq = pred_fluid_abs + pred_core_abs

        return pred_lod_seq

    def compute_mae_for_year(self, model_fluid, eop_df, year, forecast_days, precomputed_data, step_days=7):
        X_f, X_static, base_f, dates, core_ls_baseline = precomputed_data
        dates_pd = pd.DatetimeIndex(dates)

        test_mask = (dates_pd.year == year) & (dates_pd.day % step_days == 0)

        if not test_mask.any():
            return None, None

        test_X_f = X_f[test_mask]
        test_X_static = X_static[test_mask]
        test_base_f = base_f[test_mask]
        test_core_ls = core_ls_baseline[test_mask]
        test_dates = dates_pd[test_mask]

        lod_fluid = (eop_df['lod_obs'] - eop_df['lod_core']).values
        lod_core = eop_df['lod_core'].values

        true_fluid_list, true_core_list = [], []
        for date in test_dates:
            idx = eop_df[eop_df['date'] == date].index[0]
            true_fluid_list.append(lod_fluid[idx: idx + forecast_days])
            true_core_list.append(lod_core[idx: idx + forecast_days])

        true_fluid_abs = np.array(true_fluid_list)
        true_core_lod = np.array(true_core_list)

        pred_lod_seq = self.predict_year(
            model_fluid, test_X_f, test_X_static, test_base_f, test_core_ls, batch_size=1024
        )

        true_lod_seq = true_fluid_abs + true_core_lod
        lod_diff = true_lod_seq - pred_lod_seq
        ut1_error = np.cumsum(lod_diff, axis=1)
        final_errors = np.abs(ut1_error[:, -1])

        return np.mean(final_errors), final_errors

    def run(self):
        print("\n" + "=" * 70)
        print("🚀 Starting batch model testing - multi-year forecast evaluation")
        print("=" * 70)

        processor = DataProcessor(self.config)
        eop_df, nino_df, ex_df, geo_df = processor.load_all_data()

        for forecast_days in self.forecast_days_list:
            print(f"\n{'=' * 50}")
            print(f"📊 Testing forecast horizon: {forecast_days} days")
            print(f"{'=' * 50}")

            model_fluid = self._init_fluid_model(forecast_days)

            if not self._load_fluid_weights(model_fluid, forecast_days):
                continue

            print(f"   ⏳ Precomputing global test features and LS baseline matrix...")
            precomputed_data = self.prepare_test_data(
                eop_df, ex_df, nino_df, forecast_days, lookback=365
            )

            year_maes = {}
            all_errors_global = []
            for year in tqdm(self.test_years, desc=f"  Testing {forecast_days}d"):
                mae, errors = self.compute_mae_for_year(
                    model_fluid, eop_df, year, forecast_days, precomputed_data
                )
                if mae is not None:
                    year_maes[year] = mae
                    all_errors_global.extend(errors.tolist())

            self.results[forecast_days] = {
                'yearly': year_maes,
                'global_errors': all_errors_global
            }

            print(f"\n   📋 {forecast_days}-day forecast - Yearly MAE:")
            for year in sorted(year_maes.keys()):
                print(f"   {year:<6} | {year_maes[year]:<12.3f}")
            if all_errors_global:
                print(f"   {'Global':<6} | {np.mean(all_errors_global):<12.3f}")

        # 原调用 self.plot_results() ← 删除
        self.save_results_to_csv()
        return self.results

    def save_results_to_csv(self):
        if not self.results: return
        all_years = sorted(set().union(*[set(r['yearly'].keys()) for r in self.results.values()]))
        data = {'Year': all_years}
        for forecast_days, result in self.results.items():
            year_maes = result['yearly']
            data[f'{forecast_days}d'] = [year_maes.get(y, np.nan) for y in all_years]
        df = pd.DataFrame(data)

        # 年际统计（每年等权重）
        stats_annual = {'Year': 'Annual avg'}
        for forecast_days in self.results.keys():
            col_name = f'{forecast_days}d'
            values = [v for v in df[col_name].dropna()]
            stats_annual[col_name] = f"{np.mean(values):.3f}±{np.std(values):.3f}" if values else "N/A"

        # 全样本平均（所有样本直接求均值）
        stats_global = {'Year': 'Global avg'}
        for forecast_days, result in self.results.items():
            errors = result['global_errors']
            stats_global[f'{forecast_days}d'] = f"{np.mean(errors):.3f}" if errors else "N/A"

        df = pd.concat([df, pd.DataFrame([stats_annual]), pd.DataFrame([stats_global])], ignore_index=True)

        timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
        save_path = f'./results/batch_test_results_{self.model_type_fluid}_{timestamp}.csv'
        os.makedirs('./results', exist_ok=True)
        df.to_csv(save_path, index=False)
        print(f"📊 Results saved: {save_path}")


